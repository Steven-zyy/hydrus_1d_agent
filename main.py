"""Command-line entry point — milestones 1 through 7.

Exit codes:
    0  success
    1  config load/validation error
    2  run folder already exists (use --overwrite-run or --clean-run-folder)
    3  HYDRUS_EXE missing or invalid
    4  unsupported configuration feature for the adapter
    5  inputs/outputs missing
    6  runner pre-flight failure or timeout
    7  HYDRUS executed but failed (non-zero exit or false-success pattern,
       OR a hard step in --all failed)
"""

