def _check_review_consistency(config_path: Path) -> int:
    from hydrus_agent.review_state import (
        build_requested_state,
        format_state_summary,
        read_review_state,
        states_match,
    )

    reviewed = read_review_state(REVIEW_STATE_DIR)
    if reviewed is None:
        return 0

    try:
        requested_config = load_config(config_path)
    except ConfigError as exc:
        print(f"[ERROR] {exc}", file=sys.stderr)
        return 1

    requested = build_requested_state(
        config=requested_config,
        config_path=config_path,
    )
    if states_match(reviewed, requested):
        return 0

    print("[ERROR] The config being run is not the last reviewed config.")
    print(format_state_summary("Last reviewed config", reviewed))
    print(format_state_summary("Requested config", requested))
    print("Pass --allow-config-mismatch to run this config anyway.")
    return 1


def _diagnose_run(case_dir: Path, *, launch_mode="argv") -> int:
    from hydrus_agent.runner import (
        LEVEL_01_DIR_FILENAME, TIMEOUT_MARKER, inventory_dir,
    )

    project_dir = case_dir / "hydrus_project"
    log_path = case_dir / "logs" / "hydrus_run.log"
    level_01_path = project_dir / LEVEL_01_DIR_FILENAME

    print()
    print("HYDRUS-1D run diagnostic")
    print("=" * 60)
    print(f"launch mode  : {launch_mode}")

    exe_str, source = resolve_hydrus_exe()
    if exe_str is None:
        print(f"HYDRUS_EXE   : <unset> (source: {source})")
    else:
        exe_path = Path(exe_str)
        print(f"HYDRUS_EXE   : {exe_path}")
        print(f"  source     : {source}")
        print(f"  exists     : {exe_path.exists()}")
        if exe_path.is_file():
            print(f"  size_bytes : {exe_path.stat().st_size}")
        if launch_mode == "argv":
            preview_cmd = [str(exe_path), str(project_dir.resolve()), "-1"]
        else:
            preview_cmd = [str(exe_path)]
        print(f"  would run  : {preview_cmd}")
        print(f"  cwd        : {project_dir}")

    print()
    print(f"Run folder   : {case_dir}")
    print(f"  exists     : {case_dir.exists()}")
    print(f"Project dir  : {project_dir}")
    print(f"  exists     : {project_dir.exists()}")

    selector = project_dir / "SELECTOR.IN"
    profile = project_dir / "PROFILE.DAT"
    print()
    sel_part = f" ({selector.stat().st_size} bytes)" if selector.is_file() else ""
    pro_part = f" ({profile.stat().st_size} bytes)" if profile.is_file() else ""
    print(f"SELECTOR.IN  : {'present' if selector.is_file() else 'MISSING'}{sel_part}")
    print(f"PROFILE.DAT  : {'present' if profile.is_file() else 'MISSING'}{pro_part}")
    if level_01_path.is_file():
        body = level_01_path.read_text(encoding="utf-8", errors="replace").rstrip()
        print(f"LEVEL_01.DIR : present ({level_01_path.stat().st_size} bytes)")
        print(f"  contents   : {body!r}")
    else:
        print("LEVEL_01.DIR : MISSING (will be auto-generated by --run)")

    print()
    print("Pre-run file inventory:")
    inventory = inventory_dir(project_dir)
    if not inventory:
        print("  (project dir is empty or missing)")
    else:
        for fi in inventory:
            print(fi.format_line())

    print()
    if log_path.is_file():
        log_text = log_path.read_text(encoding="utf-8", errors="replace")
        timed_out = TIMEOUT_MARKER in log_text
        print(f"Previous log : {log_path}")
        print(f"  size_bytes : {log_path.stat().st_size}")
        print(f"  timed out  : {timed_out}")
        print("  first lines:")
        for ln in log_text.splitlines()[:8]:
            print(f"    {ln}")
    else:
        print(f"Previous log : (none at {log_path})")

    print()
    print("=" * 60)
    print("Diagnostic complete. HYDRUS was NOT invoked.")
    return 0


