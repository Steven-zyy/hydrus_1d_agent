def _describe_config(
    description: str,
    write_config_path: Path | None,
    *,
    review: bool = False,
    run_after_review: bool = False,
    overwrite_run: bool = False,
    timeout=None,
    launch_mode: str = "argv",
    allow_config_mismatch: bool = False,
    field_data_path: Path | None = None,
) -> int:
    if write_config_path is None:
        print("[ERROR] --describe requires --write-config <path>.", file=sys.stderr)
        return 1

    from hydrus_agent.config_builder import (
        ConfigBuildError,
        build_config_from_description,
        summarise_built_config,
        write_config,
    )

    case_id = write_config_path.stem or "from_description"
    try:
        result = build_config_from_description(description, case_id=case_id)
    except ConfigBuildError as exc:
        print(f"[ERROR] Could not build config: {exc}", file=sys.stderr)
        return 1

    path = write_config(result.config, write_config_path)
    if review or run_after_review:
        from hydrus_agent.review_state import write_review_state
        write_review_state(
            config=result.config,
            config_path=path,
            state_dir=REVIEW_STATE_DIR,
        )
    summary = summarise_built_config(result)
    if run_after_review:
        summary = summary.replace(
            "HYDRUS was not run.",
            "HYDRUS will run only because --run-after-review was provided.",
        )
    print(summary)
    if review or run_after_review:
        print("Validation status: valid ModelConfig")
    print(f"Config written to: {path}")
    if run_after_review:
        if not allow_config_mismatch:
            rc = _check_review_consistency(path)
            if rc != 0:
                return rc
        print("Running full pipeline from reviewed config.")
        return _call_run_all(
            path,
            overwrite_run=overwrite_run,
            timeout=timeout,
            launch_mode=launch_mode,
            field_data_path=field_data_path,
        )
    return 0


