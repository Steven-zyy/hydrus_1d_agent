def _format_config_review(config) -> str:
    ic = config.initial_condition
    if ic.profile:
        points = ", ".join(f"{p.value} m at {p.depth} m" for p in ic.profile)
        ic_line = f"  initial cond : {ic.type.value} profile ({points})"
    else:
        ic_line = f"  initial cond : {ic.type.value} = {ic.value}"

    lines = [
        "Reviewed HYDRUS-1D config",
        f"  project_name : {config.project_name}",
        f"  case_id      : {config.case_id}",
        f"  sim window   : {config.simulation_time.t_init} to "
        f"{config.simulation_time.t_end} {config.simulation_time.units.value}",
        f"  soil layers  : {len(config.soil_profile)}",
        f"  materials    : {len(config.van_genuchten)}",
        f"  upper BC     : {config.upper_boundary.type.value}",
        f"  lower BC     : {config.lower_boundary.type.value}",
        ic_line,
        f"  obs depths   : {config.observation_depths}",
        f"  print times  : {config.output_settings.print_times}",
    ]
    if config.atmospheric is not None and config.atmospheric.source_metadata is not None:
        meta = config.atmospheric.source_metadata
        path_kind = "absolute" if Path(meta.source_csv).is_absolute() else "relative"
        lines.extend([
            "  atmospheric CSV:",
            f"    path        : {meta.source_csv} ({path_kind})",
            f"    records     : {meta.record_count}",
            f"    time range  : {meta.time_range[0]} to {meta.time_range[1]} {meta.time_unit}",
            f"    total precip: {meta.total_precipitation} m",
            f"    total evap  : {meta.total_potential_evaporation} m",
            f"    max precip  : {meta.max_precipitation_rate} {meta.rate_unit}",
            f"    max evap    : {meta.max_potential_evaporation_rate} {meta.rate_unit}",
            f"    units       : time={meta.time_unit}, length={meta.length_unit}, rate={meta.rate_unit}",
            "    covers end  : "
            f"{'yes' if meta.covers_simulation_end_time else 'no'}",
        ])
    if config.material_source is not None:
        meta = config.material_source
        path_kind = "absolute" if Path(meta.source_csv).is_absolute() else "relative"
        lines.extend([
            "  material CSV:",
            f"    path        : {meta.source_csv} ({path_kind})",
            f"    materials   : {meta.material_count}",
            f"    names       : {', '.join(meta.material_names)}",
            "    units       : "
            f"theta={meta.theta_unit}, alpha={meta.alpha_unit}, "
            f"Ks={meta.ks_unit}, l={meta.l_unit}",
        ])
        for material in meta.materials:
            lines.append(
                "    "
                f"{material.material_id}: {material.name} "
                f"theta_r={material.theta_r} theta_s={material.theta_s} "
                f"alpha={material.alpha} {meta.alpha_unit} "
                f"n={material.n} Ks={material.Ks} {meta.ks_unit} "
                f"l={material.l}"
            )
    return "\n".join(lines)


